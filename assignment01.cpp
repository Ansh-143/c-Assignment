# include<iostream>
using namespace std;
int main()
 {
    int num;
    cout<<"Enter the number :- "<<endl;
    cin>>num;
    if ((num%2)==0){
        cout<<"You entered an Even Number"<<endl;
    } 
   else{
        cout<<"You have entered a ODD Number"<<endl;
    }
    return 0;
 } 

